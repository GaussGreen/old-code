/// callput =  1  for call
/// callput = -1  for put

///  optiontype=0	Digital			(condition S1-S2-k>=0)
///  optiontype=1	pays S1			(condition S1-S2-k>=0)
///  optiontype=	pays S2			(condition S1-S2-k>=0)
///  optiontype=	pays S1-S2-k	(condition S1-S2-k>=0)


/////////////////////////////////////////////////////////////////////////////////////////////////////
///
///   Forward value
///
///
/////////////////////////////////////////////////////////////////////////////////////////////////////

double Export_Normal_SpreadOption(double S1,double S2,double sig1,double sig2,double rho,double k,double t,
								  int callput,int optiontype);


/////////////////////////////////////////////////////////////////////////////////////////////////////
///
///   Forward value : the smile is brought by the its slope (2 additional parameters slope1 and slope2)
///
///
/////////////////////////////////////////////////////////////////////////////////////////////////////
double Export_Smiled_Normal_SpreadOption(double S1,double S2,double sig1,double sig2,double rho,double k,
										 double t,double slope1, double slope2,int callput,int optiontype);


/////////////////////////////////////////////////////////////////////////////////////////////////////
///  
///			1st Derivatives  
///
/////////////////////////////////////////////////////////////////////////////////////////////////////
double Export_Normal_SpreadOption(int i,double S1,double S2,double sig1,double sig2,double rho,double k,
								  double t,int callput,int optiontype);

/////////////////////////////////////////////////////////////////////////////////////////////////////
///
///   1st Derivatives : the smile is brought by the its slope (2 additional parameters slope1 and slope2)
///
///
/////////////////////////////////////////////////////////////////////////////////////////////////////
double Export_Smiled_Normal_SpreadOption(int i,double S1,double S2,double sig1,double sig2,double rho,double k,
										 double t,double slope1, double slope2 ,int callput,int optiontype);



/////////////////////////////////////////////////////////////////////////////////////////////////////
///  
///			2nd Derivatives  
///
/////////////////////////////////////////////////////////////////////////////////////////////////////
